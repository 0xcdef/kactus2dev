#include "Vector.h"

//-----------------------------------------------------------------------------
// Function: Vector::Vector()
//-----------------------------------------------------------------------------
Vector::Vector(QString left, QString right):
Range(left, right)
{
    
}
//-----------------------------------------------------------------------------
// Function: Vector::Vector()
//-----------------------------------------------------------------------------
Vector::Vector(Vector const& other):
Range(other.getLeft(), other.getRight()),
vectorId_(other.vectorId_)
{

}

//-----------------------------------------------------------------------------
// Function: Vector::operator=()
//-----------------------------------------------------------------------------
Vector& Vector::operator=(Vector const& other)
{
    if (this != &other)
    {
        Range::operator=(other);
        vectorId_ = other.vectorId_;
    }

    return *this;
}

//-----------------------------------------------------------------------------
// Function: Vector::~Vector()
//-----------------------------------------------------------------------------
Vector::~Vector()
{
    
}

//-----------------------------------------------------------------------------
// Function: Vector::setId()
//-----------------------------------------------------------------------------
void Vector::setId(QString const& id)
{
    vectorId_ = id;
}

//-----------------------------------------------------------------------------
// Function: Vector::getId()
//-----------------------------------------------------------------------------
QString Vector::getId() const
{
    return vectorId_;
}
